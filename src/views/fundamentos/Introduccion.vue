    <h2 class="mt-4 mb-3">Qué es la accesibilidad digital</h2>
    <p>
      Podemos definir la accesibilidad digital como el grado en el que todas las personas pueden
      <strong>percibir, comprender, utilizar y navegar</strong> por un contenido o servicio digital
      independientemente de sus capacidades <strong>físicas, sensoriales o cognitivas</strong>, del
      dispositivo que utilicen o del contexto en el que accedan.
    </p>
    <p>
      La Web nació con vocación universal. Cuando una página, una aplicación o un documento
      respetan ese objetivo y no dejan fuera a parte de sus usuarios, podemos decir que son
      accesibles.
    </p>

    <h2 class="mt-4 mb-3">Por qué es importante</h2>
    <p>
      La accesibilidad no es un añadido ni una capa de calidad opcional. Es una condición básica
      para que la información y los servicios puedan usarse de forma real por todas las personas.
    </p>
    <p>Esto tiene consecuencias muy concretas en varios niveles:</p>
    <ul>
      <li><strong>Personas</strong>: facilita el acceso a información, trámites, formación, salud, empleo y servicios.</li>
      <li><strong>Organizaciones</strong>: mejora la calidad, amplía el alcance, reduce incidencias y ayuda a cumplir la normativa.</li>
      <li><strong>Sociedad</strong>: reduce barreras y favorece una participación más igualitaria en el entorno digital.</li>
    </ul>
    <p>
      Además, muchas decisiones de accesibilidad mejoran también la <strong>usabilidad</strong>:
      contenidos más claros, formularios mejor etiquetados, navegación más predecible o contrastes
      más legibles suelen beneficiar a todo el mundo.
    </p>

    <h2 class="mt-4 mb-3">A quién beneficia</h2>
    <p>
      Solemos agrupar las necesidades de accesibilidad en cuatro grandes bloques, aunque en la
      práctica el impacto es mucho más amplio:
    </p>
    <ul>
      <li><strong>Visual</strong>: ceguera, baja visión, visión reducida, sensibilidad al contraste o dificultad para distinguir determinados colores.</li>
      <li><strong>Auditiva</strong>: sordera total o parcial, dificultad para entender el habla o dependencia de subtítulos y transcripciones.</li>
      <li><strong>Motora</strong>: limitaciones en el uso de manos, brazos o voz, movilidad reducida o necesidad de navegar sin ratón.</li>
      <li><strong>Cognitiva</strong>: dificultades de comprensión, memoria, atención, aprendizaje o lectura.</li>
    </ul>
    <p>Pero también beneficia a muchas situaciones de uso no permanentes:</p>
    <ul>
      <li>Personas mayores cuyas capacidades cambian con la edad.</li>
      <li>Personas con limitaciones temporales, por ejemplo por un brazo inmovilizado o por la pérdida de unas gafas.</li>
      <li>Personas con limitaciones situacionales, como estar al sol, en un entorno ruidoso o usando el móvil con una sola mano.</li>
      <li>Personas con mala conexión, pantallas pequeñas o dispositivos de entrada distintos al teclado y ratón tradicionales.</li>
    </ul>

    <h2 class="mt-4 mb-3">Cómo acceden las personas al contenido</h2>
    <p>
      Las personas con discapacidad utilizan productos de apoyo o funciones de accesibilidad del
      sistema para acceder al contenido digital. No son casos raros ni excepcionales: forman parte
      del uso real de la tecnología.
    </p>
    <ul>
      <li>Si no puedo ver, puedo utilizar un lector de pantalla.</li>
      <li>Si veo con dificultad, puedo usar ampliación, alto contraste o ajustes de tamaño y espaciado.</li>
      <li>Si no puedo escuchar un vídeo, necesito subtítulos o transcripción.</li>
      <li>Si no puedo usar bien las manos, puedo navegar con teclado, conmutadores, dictado o control por voz.</li>
      <li>Si me cuesta comprender lo que leo, necesito contenido más claro, bien estructurado y predecible.</li>
    </ul>

    <h2 class="mt-4 mb-3">Funciones de accesibilidad que hoy utilizamos todos</h2>
    <p>Muchas funciones nacidas para mejorar la accesibilidad forman parte ya del uso cotidiano:</p>
    <ul>
      <li>Síntesis de voz en GPS, asistentes o lectores automáticos.</li>
      <li>Reconocimiento del habla para dictar o lanzar acciones.</li>
      <li>Subtítulos automáticos o manuales en vídeos y reuniones.</li>
      <li>Opciones de zoom, modo oscuro, ajuste de tamaño de texto o filtros de color.</li>
    </ul>
    <p>
      Por eso conviene entender la accesibilidad no solo como atención a un colectivo concreto, sino
      como una mejora general de acceso, comprensión y comodidad de uso.
    </p>

    <h2 class="mt-4 mb-3">Qué implica en un entorno digital real</h2>
    <p>Llevado a la práctica, hablar de accesibilidad significa comprobar cosas bastante concretas:</p>
    <ul>
      <li>que la estructura del contenido sea clara y semántica,</li>
      <li>que las imágenes y elementos no textuales tengan alternativa cuando la necesiten,</li>
      <li>que los formularios, enlaces y botones se entiendan y se puedan usar bien,</li>
      <li>que toda la interfaz funcione con teclado,</li>
      <li>que el foco, los mensajes y los cambios dinámicos se perciban correctamente,</li>
      <li>y que el contenido resulte legible, comprensible y predecible.</li>
    </ul>
    <p>
      La accesibilidad no se limita a añadir un texto alternativo o pasar una herramienta. Implica
      construir bien el contenido, la interacción y la semántica desde el principio.
    </p>

    <h2 class="mt-4 mb-3">Barreras frecuentes y situación actual</h2>
    <p>
      El estado actual de accesibilidad en la web sigue siendo claramente mejorable. El informe
