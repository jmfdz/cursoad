    </p>
    <p>La UX abarca la usabilidad, pero también considera:</p>
    <ul>
      <li>El diseño visual.</li>
      <li>El contenido.</li>
      <li>La accesibilidad.</li>
      <li>La credibilidad.</li>
      <li>El rendimiento.</li>
      <li>Las emociones que genera.</li>
    </ul>
    <p>
      Dicho de otro modo, una interfaz puede ser usable en términos funcionales y, sin embargo,
