    </p>

    <h2>Diseño inclusivo</h2>
    <p>
      El diseño inclusivo busca aprovechar toda la diversidad humana al concebir productos y
      servicios, de modo que funcionen para personas con diferentes capacidades, contextos y
      experiencias. Microsoft lo ha desarrollado y difundido mediante su
      <a href="https://devblogs.microsoft.com/premier-developer/microsoft-inclusive-design/" target="_blank" rel="noreferrer">metodología de diseño inclusivo (Inglés)</a>,
      que se ha convertido en una referencia para muchos equipos y universidades.
    </p>
    <p>
      La propuesta parte de entender la discapacidad como un desajuste entre la persona y el
      entorno, y anima a quienes diseñan a detectar esos desajustes desde el inicio del proceso, no
      como un añadido posterior, para que la accesibilidad se traduzca en mejores experiencias para
      todas las personas.
    </p>
    <p>El marco de Microsoft se articula en tres principios clave:</p>
    <ul>
      <li>Reconocer la exclusión: mapear dónde y por qué un sistema deja fuera a alguien.</li>
      <li>Aprender de la diversidad: incorporar las perspectivas de quienes viven esas exclusiones para revelar soluciones creativas.</li>
      <li>Crear para uno, extender a muchos: resolver bien un caso extremo y escalar la solución porque beneficiará a un espectro amplio de usuarios.</li>
    </ul>
    <p>
      A estos se suman pautas operativas como pensar de forma universal y diseñar con múltiples
      formas de participación, que guían a los equipos durante todo el ciclo de vida del producto.
    </p>

    <h2>Usabilidad</h2>
    <p>
      La usabilidad se refiere a cómo de <strong>eficaz, eficiente y satisfactoria</strong> es la
      interacción de personas concretas con un sitio web o aplicación, en un contexto de uso
      determinado. Una interfaz usable ayuda a lograr los objetivos con el menor esfuerzo y con
      errores fáciles de evitar o corregir.
    </p>
    <p>Principios clave de la usabilidad:</p>
    <ul>
      <li>Facilidad de aprendizaje: el usuario entiende rápidamente cómo usar el sistema.</li>
      <li>Eficiencia: una vez aprendido, el sistema permite realizar tareas rápidamente.</li>
      <li>Memorabilidad: el usuario puede recordar cómo usarlo tras un tiempo sin utilizarlo.</li>
      <li>Bajo índice de errores: pocos errores y fáciles de corregir.</li>
      <li>Satisfacción: el uso del sistema resulta agradable.</li>
    </ul>

    <h2>Experiencia de usuario (UX)</h2>
    <p>
      La experiencia de usuario es más amplia: engloba todas las <strong>emociones, percepciones y
      reacciones</strong> que una persona tiene al interactuar con una página web o aplicación.
    </p>
    <p>La UX abarca la usabilidad, pero también considera:</p>
    <ul>
      <li>El diseño visual.</li>
      <li>El contenido.</li>
      <li>La accesibilidad.</li>
      <li>La credibilidad.</li>
      <li>El rendimiento.</li>
      <li>Las emociones que genera.</li>
    </ul>
    <p>
      Dicho de otro modo, una interfaz puede ser usable en términos funcionales y, sin embargo,
      generar una mala experiencia si resulta confusa, poco creíble, visualmente descuidada o
      excesivamente fría en la interacción.
    </p>

    <h2>Cómo evaluar la usabilidad y la experiencia</h2>
    <p>
      Para comprobar si nuestros diseños son usables y ofrecen una buena experiencia de usuario,
      podemos utilizar diferentes técnicas y herramientas:
    </p>
    <ul>
      <li><strong>Tests de usuario</strong>: observar cómo personas reales interactúan con el sitio.</li>
      <li><strong>Análisis de tareas</strong>: identificar los pasos necesarios para completar una tarea.</li>
      <li><strong>Prototipos</strong>: probar la estructura antes de desarrollar.</li>
      <li><strong>Mapas de calor</strong>: visualizar patrones de clic o desplazamiento; no sustituyen la observación directa.</li>
      <li><strong>Encuestas y entrevistas</strong>: recopilar opiniones y emociones.</li>
      <li><strong>Pruebas A/B</strong>: comparar dos versiones según un objetivo medible.</li>
      <li><strong>Personas</strong>: representar necesidades y comportamientos detectados mediante investigación.</li>
    </ul>
    <p>
      No es necesario utilizar todas las técnicas en todos los proyectos. Conviene elegirlas según
      el objetivo, la fase del diseño y los recursos disponibles.
    </p>
